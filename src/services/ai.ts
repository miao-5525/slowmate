/**
 * 「慢慢来」AI 陪练 —— 智能对话模拟引擎
 *
 * 每轮对话返回 AI 回复 + 后续可选回复，试用者点一下就能继续聊。
 */

export interface AIResponse {
  content: string;
  suggestions: string[];
}

// ==========================================
// 对话树
// ==========================================

function reply(input: string): AIResponse {
  const msg = input.trim();

  // ====== 问候 ======
  if (/^(你好|您好|嗨|早上好|下午好|晚上好|在吗|在不在|hi|hello)/i.test(msg)) {
    const h = new Date().getHours();
    const g = h < 12 ? '上午好' : h < 18 ? '下午好' : '晚上好';
    return {
      content: `${g}！😊 我是您的数字生活陪练"小慢"。\n\n不着急，咱们慢慢学。您想了解什么？`,
      suggestions: [
        '怎么用手机挂号？🏥',
        '怎么缴水电费？💡',
        '怎么用手机打车？🚗',
        '怎么跟家人视频？📞',
        '帮我看短信是不是诈骗 🛡️',
        '想学在网上买东西 🛒',
      ],
    };
  }

  // ====== 挂号流程 ======
  if (/挂号|医院|看病|预约/.test(msg)) {
    if (/第.*1|第一|开始|第一步|怎么/.test(msg) || !/第/.test(msg)) {
      return {
        content:
          '好的，咱们来学「🏥 线上挂号」！\n\n第 1 步：打开微信，点顶部的 🔍 搜索框，输入您要去的医院名字。\n\n找到医院公众号后点「关注」——注意看名字旁边有没有蓝色 ✓ 认证标志哦。',
        suggestions: ['找到了！然后呢？', '找不到这个医院 😟', '什么是认证标志？'],
      };
    }
    if (/找到了|然后|接着/.test(msg)) {
      return {
        content:
          '太棒了！👍\n\n第 2 步：进入公众号，点底部菜单栏的「就医服务」，再选「预约挂号」。一般在底部菜单最左边。\n\n您看到了吗？',
        suggestions: ['看到了！有预约挂号', '没看到就医服务 😟', '点进去要填信息，怎么办？'],
      };
    }
    if (/看到|预约挂号/.test(msg)) {
      return {
        content:
          '很好！👏\n\n第 3 步：在科室列表里找到您要看的科。\n\n比如感冒看「内科」，牙疼看「口腔科」。不确定挂哪个科就选「全科门诊」。',
        suggestions: ['选好科室了！下一步？', '我不确定该挂哪个科 🤔', '科室太多了看花眼了'],
      };
    }
    if (/选好|不确定|看花眼/.test(msg)) {
      return {
        content:
          '没关系～不确定就选「全科门诊」或者「普通内科」，到了医院医生会根据您的情况帮您转科的。\n\n第 4 步：选好科室后，挑一个有空的日子（绿色有号、灰色没号），再选一位医生。\n\n🛡️ 安全提醒：确认挂号前一定核对日期、医生名字。',
        suggestions: ['选好了！怎么付钱？', '都是灰色的，没号了 😟', '挂号要多少钱？'],
      };
    }
    if (/付钱|没号|多少钱/.test(msg)) {
      return {
        content:
          '挂号费一般在 ¥10-50 左右，微信支付就行。\n\n如果今天没号了，可以试试往后翻几天，或者换个医院看看。\n\n第 5 步（最后一步）：核对信息都对，点「确认挂号」付款就行。\n\n🛡️ 正规挂号平台不会让您额外转账给个人，遇到这种情况马上停止！',
        suggestions: ['学会了！谢谢小慢 🎉', '我想学别的', '再学一遍挂号'],
      };
    }
  }

  // ====== 缴费流程 ======
  if (/缴费|水费|电费|燃气|煤气|话费|宽带|账单/.test(msg)) {
    return {
      content:
        '好的，咱们来学「💡 生活缴费」！\n\n第 1 步：打开微信 → 点右下角「我」→ 点「服务」→ 找到「生活缴费」点进去。\n\n您会看到水费、电费、燃气费等图标。\n\n🛡️ 注意：只用微信或支付宝里的官方缴费入口，不要扫陌生人给的二维码！',
      suggestions: ['找到了！然后呢？', '要输入什么户号，在哪找？', '没用过，有点怕点错 😟'],
    };
  }

  // ====== 打车流程 ======
  if (/打车|出行|滴滴|叫车|导航|出门/.test(msg)) {
    return {
      content:
        '好的，咱们来学「🚗 打车出行」！\n\n第 1 步：打开微信，在聊天列表页面往下拉一下，会出现「最近使用的小程序」。找到「滴滴出行」点开。\n\n手机会自动定位您在哪儿。您只需要在"您去哪儿"输入框里输入要去的地方。\n\n🛡️ 上车前一定核对手机上显示的车牌号！',
      suggestions: ['打开了！然后怎么叫车？', '怎么知道要多少钱？', '上车要注意什么？🛡️'],
    };
  }

  // ====== 视频通话 ======
  if (/视频|视频通话|视频聊天|打.*电话|见.*面/.test(msg)) {
    return {
      content:
        '好的，咱们来学「📞 视频通话」！\n\n第 1 步：打开微信 → 点底部「通讯录」→ 找到您想视频通话的人。\n\n或者直接在聊天列表里，点开和 ta 的对话框。\n\n温馨提示：用家里的 WiFi 打视频不费流量哦～',
      suggestions: ['找到了！怎么打视频？', '对方能看到我吗？', '打视频要花钱吗？'],
    };
  }

  // ====== 购物 ======
  if (/购物|买.*东西|淘宝|拼多多|快递|下单/.test(msg)) {
    return {
      content:
        '好的，咱们来学「🛒 手机购物」！\n\n第 1 步：打开淘宝或拼多多 App。在顶部搜索框打字搜您想买的东西，比如"老年健步鞋"。\n\n搜出来以后，可以上下滑动看更多商品，点图片能看大图和详细介绍。\n\n🛡️ 安全提醒：只在官方 App 里买东西！不要点别人发来的链接付款！',
      suggestions: ['怎么选尺码和颜色？', '怎么付钱安全？', '买了不喜欢怎么退？'],
    };
  }

  // ====== 防骗 ======
  if (/诈骗|骗子|防骗|验证码|转账|汇款|中奖|陌生.*链接|密码|银行卡|安全账户/.test(msg)) {
    if (/验证码/.test(msg)) {
      return {
        content:
          '⚠️ 验证码非常重要！记住：\n\n验证码就像您家门的钥匙 🔑，给别人就等于把门打开让人随便进！\n\n银行、公安局、法院绝对不会打电话找您要验证码。任何人要验证码，都是骗子！\n\n如果已经不小心给了验证码，马上打银行客服电话冻结账户！',
        suggestions: ['知道了！还有什么要注意？', '有人打电话说是警察要我配合 😟', '收到过中奖短信，是真的吗？'],
      };
    }
    return {
      content:
        '🛡️ 防骗是最重要的一课！记住三个"绝对不"：\n\n❶ 验证码绝对不告诉任何人\n❷ 绝对不给陌生人转账\n❸ 陌生链接绝对不点\n\n常见骗术：\n• "您中奖了，先交税"——骗人的\n• "我是您孙子，急用钱"——先打给子女确认\n• "医保卡被停"——打 12333 核实\n\n遇到拿不准的，先挂电话、问子女、或打 96110！',
      suggestions: ['如果有陌生人让我转账怎么办？', '收到可疑短信怎么判断？', '被骗了怎么办？😟'],
    };
  }

  // ====== 被诈骗了怎么办 ======
  if (/被骗|报警|110/.test(msg)) {
    return {
      content:
        '如果不小心被骗了，别慌，记住三步：\n\n1️⃣ 马上打 110 报警\n2️⃣ 联系银行冻结账户\n3️⃣ 保留聊天记录和转账截图作为证据\n\n时间越短，追回钱的可能性越大。\n\n不要觉得不好意思，骗子就是利用了人的善良和信任。告诉家人，他们会帮您的。',
      suggestions: ['谢谢提醒 🙏', '太可怕了，我想再多学学防骗', '换一个话题'],
    };
  }

  // ====== 完成 / 感谢 ======
  if (/谢谢|感谢|学会了|太.*好|真棒|厉害|完成/.test(msg)) {
    return {
      content:
        '不客气！😊 您真厉害，又学到了一项新技能！\n\n每天学一点，慢慢来，您会越来越熟练的。\n\n以后遇到什么手机上的问题，随时来找小慢～',
      suggestions: ['再学一个！', '我想学挂号 🏥', '想了解防骗知识 🛡️', '怎么打视频电话 📞'],
    };
  }

  // ====== 困难 / 重来 ======
  if (/不会|不懂|不明白|再说|没找到|找不到|没有|太难|重来/.test(msg)) {
    return {
      content:
        '没关系的，这个很正常～我用手机好多年了，有时候也会找不到按钮 😅\n\n咱们慢一点：\n1. 先把手机放桌上\n2. 告诉我屏幕上现在能看到什么？\n3. 最上面写了什么字？\n\n我帮您找！',
      suggestions: ['屏幕上是微信的聊天页面', '我在设置页面迷路了', '算了换一个简单点的吧'],
    };
  }

  // ====== 默认引导 ======
  return {
    content:
      '您想学点什么呢？我一步一步陪着您练～\n\n选一个试试吧 👇',
    suggestions: [
      '怎么用手机挂号？🏥',
      '怎么缴水电费？💡',
      '怎么用手机打车？🚗',
      '怎么跟家人视频？📞',
      '帮我看短信是不是诈骗 🛡️',
      '想学在网上买东西 🛒',
    ],
  };
}

// ==========================================
// 对外接口
// ==========================================

export function getMockReply(userInput?: string): AIResponse {
  return reply(userInput || '');
}

export function getMockReplyAsync(userInput?: string): Promise<AIResponse> {
  return new Promise((resolve) => {
    setTimeout(() => resolve(reply(userInput || '')), 600 + Math.random() * 800);
  });
}
